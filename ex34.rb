# Ordinal indicates position, index at zero.
# Cardinal indicates count.
# Remember: ordinal == ordered, 1st; cardinal == cards at random, 0.

# Let's practice this. 
# Take this array of animals, and follow the exercises where I tell you to write down what animal you get for that ordinal or cardinal number. 
# Remember, if I say "first," "second," then I'm using ordinal, so subtract 1. 
# If I give you cardinal (like "The animal at 1"), then use it directly.

animals = ['bear', 'ruby', 'peacock', 'kangaroo', 'whale', 'platypus']

# The animal at 1.
